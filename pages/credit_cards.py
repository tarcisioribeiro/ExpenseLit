    def _create_card(self, card_data: Dict[str, Any]) -> None:
        """Cria um novo cart√£o de cr√©dito."""
        try:
            with st.spinner("üíæ Criando cart√£o..."):
                time.sleep(1)
                new_card = api_client.post("credit-cards/", card_data)
                print(new_card)

            st.toast("‚úÖ Cart√£o criado com sucesso!")
            time.sleep(1)
            st.info(
                "üîí **Seguran√ßa:** O CVV foi criptografado e n√£o ser√° exibido.")
            st.rerun()

        except ApiClientError as e:
            st.error(f"Erro ao criar cart√£o: {e}")
            logger.error(f"Erro ao criar cart√£o: {e}")

